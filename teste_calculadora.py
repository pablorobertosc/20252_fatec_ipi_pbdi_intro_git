import calculadora
a = 2
b = 3

soma = calculadora.soma(a, b)
print(f'{a} + {b} = {soma}')

subtrair = calculadora.subtrair(a, b)
print(f'{a} + {b} = {subtrair}')

divisão = calculadora.dividir(a, b)
print(f'{a} + {b} = {divisão}')

multiplicação = calculadora.multiplicar(a, b)
print(f'{a} + {b} = {multiplicação}')